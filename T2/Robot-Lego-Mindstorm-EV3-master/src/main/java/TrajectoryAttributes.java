public class TrajectoryAttributes {
    double yc1, yc2, xc1, xc2, r;
    int xf, yf, of;

    public TrajectoryAttributes(double yc1, double yc2, double xc1, double xc2, double r, int xf, int yf, int of) {
        this.yc1 = yc1;
        this.yc2 = yc2;
        this.xc1 = xc1;
        this.xc2 = xc2;
        this.r = r;
        this.xf = xf;
        this.yf = yf;
        this.of = of;
    }
}
